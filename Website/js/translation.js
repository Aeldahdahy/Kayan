document.addEventListener("DOMContentLoaded", function () {
    const languageLinks = document.querySelectorAll(".dropdown-menu a"); // All language dropdown items
    const languageSwitcher = document.getElementById("languageSwitcher");
    const languageSwitcher1 = document.getElementById("languageSwitcher1");

    function setLanguage(lang) {
        lang = lang.toLowerCase(); // 🔹 Ensure lowercase keys

        if (!translations[lang]) {
            console.error(`Translations for "${lang}" not found.`);
            return;
        }

        localStorage.setItem("language", lang);
        document.documentElement.lang = lang;

        document.querySelectorAll(".left-side").forEach((left) => {
            left.dir = lang === "ar" ? "rtl" : "ltr"; // Switch direction
        });

        document.querySelectorAll(".left-align").forEach((left) => {
            if (lang === "ar") {
                left.classList.add("flex-row-reverse");
                left.classList.remove("flex-row");
                left.style.textAlign = "right";
            } else {
                left.classList.add("flex-row");
                left.classList.remove("flex-row-reverse");
                left.style.textAlign = "left";
            }
        });

        let image1 = document.querySelector(".about-img-1");
        if (image1) {
            image1.style.margin = lang === "ar" ? "0 -145px 0 10px" : "0 10px 0 -145px";
            let skillImg = document.querySelector(".skill-img-1");
            if (skillImg) {
                skillImg.style.margin = lang === "ar" ? "0 0 0 -320px" : "0 -320px 0 0";
                skillImg.style.padding = lang === "ar" ? "0 30px 0 0" : "0 0 0 30px";
            }
        }
        let workImg= document.querySelector(".work-img")
        if (workImg) {
            workImg.style.margin = lang === "ar" ? "0 0 -220px -335px" : "0 -335px -336px 0";
        }

        document.querySelectorAll(".ppercent").forEach((per) => {
            per.style.right = lang === "ar" ? "90%" : "-11px";
        });

        document.documentElement.style.setProperty(
            "--dots-position", lang === "ar" ? "auto" : "0"
        );

        // ✅ Fix: Modify `.is-dots::before`
        const styleSheet = Array.from(document.styleSheets).find(sheet => sheet.href === null);
        if (styleSheet) {
            let beforeRuleIndex = Array.from(styleSheet.cssRules).findIndex(rule =>
                rule.selectorText === ".is-dots::before"
            );

            if (beforeRuleIndex !== -1) {
                let beforeRule = styleSheet.cssRules[beforeRuleIndex];
                beforeRule.style.setProperty(lang === "ar" ? "right" : "left", "0");
                beforeRule.style.setProperty(lang === "ar" ? "left" : "right", "auto");
            }
        }

        document.querySelectorAll("[data-translate]").forEach((el) => {
            const translateKey = el.dataset.translate;
            if (translations[lang][translateKey]) {
                el.textContent = translations[lang][translateKey];
            } else {
                console.warn(`Translation key "${translateKey}" not found for language "${lang}".`);
            }
        });

        document.querySelectorAll(".inpoo").forEach((el) => {
            const translateKey = el.dataset.translate;
            if (translations[lang][translateKey]) {
                el.placeholder = translations[lang][translateKey];
            }
        });

        // ✅ Update dropdown text for both switchers
        if (languageSwitcher) languageSwitcher.textContent = lang === "ar" ? "العربية" : "English";
        if (languageSwitcher1) languageSwitcher1.textContent = lang === "ar" ? "العربية" : "English";

        // ✅ Reinitialize Bootstrap dropdown (to ensure the second one works)
        document.querySelectorAll(".dropdown-toggle").forEach((dropdown) => {
            new bootstrap.Dropdown(dropdown);
        });
    }

    // ✅ Handle dropdown clicks for BOTH dropdowns
    languageLinks.forEach((link) => {
        link.addEventListener("click", function (event) {
            event.preventDefault();
            const selectedLang = this.textContent.trim().toLowerCase() === "arabic" ? "ar" : "en";
            setLanguage(selectedLang);
        });
    });

    // 🔹 Ensure saved language is lowercase before using it
    const savedLang = (localStorage.getItem("language") || "en").toLowerCase();
    setLanguage(savedLang);
});


document.querySelectorAll('.dropdown-toggle').forEach(item => {
    item.addEventListener('click', function (event) {
        event.preventDefault();
        new bootstrap.Dropdown(this).toggle();
    });
});
